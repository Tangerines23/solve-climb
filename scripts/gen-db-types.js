#!/usr/bin/env node
/**
 * Supabase Type Generator: Automated Type Sync
 * Fetches types from the local Supabase instance and updates src/types/database.types.ts
 */

import { spawnSync } from 'child_process';
import path from 'path';
import fs from 'fs';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const rootPath = path.join(__dirname, '..');
const outputPath = path.join(rootPath, 'src', 'types', 'database.types.ts');

console.log('🔄 Synchronizing Supabase types...');

// 1. Check if Supabase is running
const statusResult = spawnSync('npx', ['supabase', 'status'], { stdio: 'pipe', encoding: 'utf8' });

if (statusResult.status !== 0) {
  console.log('⚠️ Supabase is not running. Starting Supabase...');
  const startResult = spawnSync('npx', ['supabase', 'start'], { stdio: 'inherit' });
  if (startResult.status !== 0) {
    console.error('❌ Failed to start Supabase. Please ensure Docker is running.');
    process.exit(1);
  }
}

// 2. Generate types
console.log('📡 Generating types from local Supabase instance...');
const genResult = spawnSync('npx', ['supabase', 'gen', 'types', 'typescript', '--local'], {
  stdio: ['inherit', 'pipe', 'inherit'],
  encoding: 'utf8',
  maxBuffer: 1024 * 1024 * 10, // 10MB buffer for large schemas
});

if (genResult.status === 0 && genResult.stdout) {
  // Add warning comment at the top
  const finalContent = `/**
 * AUTO-GENERATED FILE - DO NOT EDIT DIRECTLY
 * This file was generated by scripts/gen-db-types.js
 * Last updated: ${new Date().toISOString()}
 */

${genResult.stdout}`;

  fs.writeFileSync(outputPath, finalContent);
  console.log(`✅ Types successfully updated at ${path.relative(rootPath, outputPath)}`);
} else {
  console.error('❌ Failed to generate types.');
  if (genResult.stderr) console.error(genResult.stderr);
  process.exit(1);
}
